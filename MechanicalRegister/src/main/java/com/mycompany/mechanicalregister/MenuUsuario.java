    public void voltar() {

        int resp = JOptionPane.showConfirmDialog(
                null,
                "Deseja realmente voltar?",
                "Voltar",
                JOptionPane.YES_NO_OPTION
        );
        if (resp == 0) {
            MechanicalRegister identificacao = new MechanicalRegister();
            identificacao.setVisible(true);
            dispose();
        }
    }

